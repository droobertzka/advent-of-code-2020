isHash x str =
    str!!(getUseableIndex x str) == '#'

calcNumHashes (x, y) numHashes (slopeX, slopeY) strings =
    if y >= length strings
    then numHashes
    else
        let nextNumHashes = if isHash x (strings!!y)
            then numHashes + 1
            else numHashes
        in calcNumHashes (x + slopeX, y + slopeY) nextNumHashes (slopeX, slopeY) strings

